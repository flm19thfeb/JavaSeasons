package may1st;

public class ConcatinationOperators {
	
	public static void main(String[] args) {
		
		String s1="New";
		String s2="Year";

		System.out.println(s1+s2);
		
		System.out.println(s1+10);
		
		System.out.println(s1+0.55);
		
		System.out.println('A'+'B');
		
		System.out.println(s1+'A');
		
		

	}

}
